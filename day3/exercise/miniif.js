// Write an if-else if-else block to print:

// "Distinction" if ≥ 80

// "Pass" if ≥ 50

// "Fail" otherwise

// Add a isLate boolean. If true, log "Submit with penalty".

let score = 56;

if (score >= 80) {
  console.log("Distinction");
} else if (score >= 50) {
  console.log("Pass");
} else {
  console.log("Fail");
}

let isLate = true;

if (isLate) {
  console.log("Submit with penalty");
}